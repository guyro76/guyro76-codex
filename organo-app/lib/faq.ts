function normalize(value: string) {
  return value.toLowerCase().replace(/[.,:;!?()[\]{}"'׳״/\\|_-]/g, " ").replace(/\s+/g, " ").trim();
}

export function findFaqAnswer(question: string): FaqItem | null {
  const query = normalize(question);
  if (!query) return null;
  let best: { item: FaqItem; score: number } | null = null;
  for (const item of faqItems) {
    const candidates = [item.question, ...item.keywords].map(normalize);
    let score = 0;
    for (const candidate of candidates) {
      if (query.includes(candidate) || candidate.includes(query)) score += 8;
      for (const word of query.split(" ").filter((part) => part.length > 1)) {
        if (candidate.includes(word)) score += 1;
      }
    }
    if (!best || score > best.score) best = { item, score };
  }
  return best && best.score >= 3 ? best.item : null;
}

